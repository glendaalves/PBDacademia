    public void preencherCadastro(Produto p) {

        getTxtNome().setText(p.getDescricao());
        getTxtvenda().setText(p.getValor_venda() + "");
        getTxtestoqueatual().setText(p.getEstoque_atual() + "");
        getTxttamanha_peso().setText(p.getTamanho_peso());

        for (int u = 0; u < getCombofornecedor().getItemCount(); u++) {

            if (getCombofornecedor().getItemAt(u).equals(p.getFornecedor())) {
                getCombofornecedor().setSelectedItem(getCombofornecedor().getItemAt(u));
            }
        }

        for (int u = 0; u < getConbogrupo().getItemCount(); u++) {

            if (getConbogrupo().getItemAt(u).equals(p.getEstoque())) {
                getConbogrupo().setSelectedItem(getConbogrupo().getItemAt(u));

            }
        }

    }

