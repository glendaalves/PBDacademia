    public void preencherCadastro(Professor p) {

        getTxtCep().setText(p.getDados().getCep());
        getTxtbairro().setText(p.getDados().getBairro());
        getTxtnumero().setText(p.getDados().getNumero() + "");
        getTxtcidade().setText(p.getDados().getCidade());
        getTxtrua().setText(p.getDados().getRua());

        for (int u = 0; u < getTxtUf().getItemCount(); u++) {

            if (getTxtUf().getItemAt(u).equals(p.getDados().getUf())) {
                getTxtUf().setSelectedItem(getTxtUf().getItemAt(u));
            }
        }

        getTxtcelular().setText(p.getDados().getCelular());
        getTxttelefone().setText(p.getDados().getTelefone());
        getTxtemail().setText(p.getDados().getEmail());

        getTxtCpf().setText(p.getCpf());
        getTxtNome().setText(p.getNome());
        getTxtrg().setText(p.getRg());
        getTxtsalario().setText(p.getSalario() + "");
        getTxtcarteiratrabalho().setText(p.getCarteira_trabalho());

        for (int u = 0; u < getCombosexo().getItemCount(); u++) {

            if (getCombosexo().getItemAt(u).equals(p.getSexo())) {
                getCombosexo().setSelectedItem(getCombosexo().getItemAt(u));
            }
        }
        
        String pwd = new String(p.getLogin().getSenha());
        getTxtusuario().setText(p.getLogin().getUsuario());
        getTxtsenha().setText(pwd);
        getTxtconfirmasenha().setText(pwd);
        getDataNascimento().setDate(p.getData_nascimento());
        

    }

